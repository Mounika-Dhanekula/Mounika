"""Create a Python list that contains the names of 5 different fruits. Perform the given operations on
the list"""
fruits = ["Apple", "Banana", "Cherry", "Mango", "Orange"]
print("List of fruits:", fruits)
fruits.append("Grapes")  
print("After adding Grapes:", fruits)
fruits.remove("Banana")  
print("After removing Banana:", fruits)
print("The first fruit in the list is:", fruits[0])  
print("The last fruit in the list is:", fruits[-1]) 
